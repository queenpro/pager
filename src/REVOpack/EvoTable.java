package REVOpack;


import java.util.ArrayList;

 
public class EvoTable {
     public ArrayList<EvoField> field;
        public String name; 
    public String ID; 

    public EvoTable() {
         field = new ArrayList<EvoField>();
    }

    
    
    
    
    public ArrayList<EvoField> getField() {
        return field;
    }

    public void setField(ArrayList<EvoField> field) {
        this.field = field;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getID() {
        return ID;
    }

    public void setID(String ID) {
        this.ID = ID;
    }
    
}
